public class Men {

    private String name;
    private Integer run = 10;
    private Integer jump = 2;


    public Men(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public Integer getRun() {
        return run;
    }

    public Integer getJump() {
        return jump;
    }
}
